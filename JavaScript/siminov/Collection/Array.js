/**
    Check whether array contains the object

    @method contains
    @param obj {Object} Object
    @return bool {Bool} true|false True if array contains the object else false
*/
Array.prototype.contains = function(obj) {
    var i = this.length;
    while (i--) {
        if (this[i] === obj) {
            return true;
        }
    }
    return false;
}


/**
    Remove the object from array

    @method remove
    @param obj {Object} Object
*/
Array.prototype.remove = function(val) {
    for(var i=0; i<this.length; i++) {
        if(this[i] == val) {
            this.splice(i, 1);
            break;
        }
    }
}